package com.example.androidchess52;

import androidx.appcompat.app.AppCompatActivity;

import android.content.Intent;
import android.os.Bundle;
import android.view.View;

public class MainActivity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
    }

    public void playGame(View view) {
        Intent i = new Intent(this, PlayGame.class);
        startActivity(i);
    }

    public void recordScreen(View view) {
        Intent i = new Intent(this, RecordController.class);
        startActivity(i);
    }

    public void viewGame(View view) {
        Intent i = new Intent(this, RecordGame.class);
        startActivity(i);
    }
}